function interpolate(template, vars = {}) {
  return String(template).replace(/\{\{(\w+)\}\}/g, (_, key) => vars[key] ?? "");
}

export function createTranslator(lang = "en") {
  const dict = translations[lang] || translations.en;
  return function t(key, vars) {
    const value = dict[key] ?? translations.en[key] ?? key;
    return interpolate(value, vars);
  };
}

export function translateCategory(lang, category) {
  const key = `category_${String(category).replace(/\s+/g, "_")}`;
  return createTranslator(lang)(key);
}
